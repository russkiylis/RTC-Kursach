function [time, dt, u, jumps, slopes, umax] = dano_createSignal(obj)
% DANO_CREATESIGNAL — создаёт кусочно-линейный входной сигнал по параметрам задания.
%
% ЧТО ДЕЛАЕТ:
%   По заданным U1, U2, U3, U4, T (3 варианта T1) и T2 строит 3 варианта
%   кусочно-линейного сигнала u(t). Каждый вариант отличается точкой перелома T1.
%
% ФОРМА СИГНАЛА:
%   Сигнал задан на отрезке [0, T2] двумя прямыми:
%
%     u(t) = U1 + (U2-U1) * t/T1,         при 0 ≤ t < T1    (первый отрезок)
%     u(t) = U3 + (U4-U3) * (t-T1)/(T2-T1), при T1 < t ≤ T2  (второй отрезок)
%     u(t) = 0,                               при t < 0 или t > T2
%
%   Если U2 ≠ U3, то в точке T1 есть скачок (разрыв сигнала).
%
%   Пример (вариант 6): U1=6, U2=6, U3=4, U4=0
%     Первый отрезок: горизонтальная прямая на уровне 6В
%     В точке T1: скачок вниз с 6В до 4В
%     Второй отрезок: линейный спад от 4В до 0В
%
% ЗАЧЕМ НУЖНЫ jumps и slopes:
%   Для аналитического расчёта спектра используется МЕТОД ДИФФЕРЕНЦИРОВАНИЯ:
%   - Кусочно-линейный сигнал дважды дифференцируют.
%   - Первая производная: ступеньки (от наклонов) + дельта-функции (от скачков).
%   - Вторая производная: только дельта-функции и их производные.
%   - Спектр второй производной легко записать аналитически.
%   - Затем "интегрируют" обратно в частотной области (делят на (jω)^2).
%
%   jumps — хранит информацию о скачках сигнала (порождают δ-функции).
%   slopes — хранит информацию о наклонах (линейных участках) сигнала.
%
% ВЫХОДНЫЕ ПАРАМЕТРЫ:
%   time   — вектор [1 x N] значений времени (с)
%   dt     — шаг дискретизации по времени (с)
%   u      — матрица [3 x N] сигналов (каждая строка — один вариант T1)
%   jumps  — cell-массив {3x1} структур с полями .time и .amplitude
%   slopes — cell-массив {3x1} структур с полями .time1, .time2, .diff
%   umax   — максимальное значение сигнала (В), берётся от первого варианта

    % Достаём параметры из объекта для удобства (чтобы не писать obj. каждый раз)
    U1 = obj.U1;
    U2 = obj.U2;
    U3 = obj.U3;
    U4 = obj.U4;
    T = obj.T;      % Вектор [1x3] — три значения T1
    T2 = obj.T2;    % Общая длительность сигнала
    N = obj.N;      % Количество точек дискретизации

    % =====================================================================
    % СОЗДАНИЕ ОСИ ВРЕМЕНИ И МАТРИЦЫ СИГНАЛОВ
    % =====================================================================
    % Ось времени чуть шире, чем [0, T2]: от -0.01*T2 до 1.01*T2,
    % чтобы сигнал корректно отображался у границ (не обрезался).
    time = linspace(-0.01.*T2, 1.01.*T2, N);
    dt = time(2)-time(1);   % Шаг дискретизации = расстояние между соседними точками
    u = zeros(length(T), length(time));  % Матрица [3 x N], заполненная нулями

    % =====================================================================
    % СОЗДАНИЕ ЛОГИЧЕСКИХ МАСОК ДЛЯ КУСОЧКОВ СИГНАЛА
    % =====================================================================
    % Для каждого из 3 вариантов T1 создаём маски (логические массивы),
    % которые показывают, какие точки оси времени принадлежат какому участку.
    % Это стандартный приём в Matlab для задания кусочных функций.
    kusok1 = false(3, length(time));    % Маска первого участка: [0, T1)
    skachok = false(3, length(time));   % Маска точки скачка: t == T1
    kusok2 = false(3, length(time));    % Маска второго участка: (T1, T2]

    for k = 1:3
        kusok1(k,:)  = (time >= 0)    & (time <  T(k));    % Первый отрезок
        skachok(k,:) = (time == T(k));                      % Точка перелома
        kusok2(k,:)  = (time >  T(k)) & (time <= T2);      % Второй отрезок
    end

    % =====================================================================
    % ЗАПОЛНЕНИЕ СИГНАЛА ПО КУСКАМ
    % =====================================================================
    % Для каждого варианта T1 вычисляем значения сигнала на каждом участке.
    % Формулы — это обычные уравнения прямых (линейная интерполяция):
    %   y = y_start + (y_end - y_start) * (t - t_start) / (t_end - t_start)
    for k = 1:3
        % Первый отрезок: прямая от (0, U1) до (T1, U2)
        u(k, kusok1(k,:))  = U1 + (U2-U1) .* time(kusok1(k,:))  / T(k);

        % Точка скачка (если U2 ≠ U3): значение сигнала принимаем равным U3
        u(k, skachok(k,:)) = U3;

        % Второй отрезок: прямая от (T1, U3) до (T2, U4)
        u(k, kusok2(k,:))  = U3 + (U4-U3) .* (time(kusok2(k,:)) - T(k)) / (T2 - T(k));
    end

    % Максимум берём от первого варианта сигнала — он нужен для расчёта W0
    umax = max(u(1,:));

    % =====================================================================
    % ОПРЕДЕЛЕНИЕ СКАЧКОВ (РАЗРЫВОВ) СИГНАЛА
    % =====================================================================
    % Скачки — это места, где сигнал имеет разрыв (мгновенно меняет значение).
    % В первой производной скачки порождают ДЕЛЬТА-ФУНКЦИИ δ(t - t_скачка),
    % амплитуда которых равна величине скачка.
    %
    % Возможные скачки:
    %   1. В точке t=0: если U1 > 0, сигнал "включается" (скачок от 0 до U1)
    %   2. В точке t=T1: если U2 ≠ U3, скачок с U2 до U3 (амплитуда = U3 - U2)
    %   3. В точке t=T2: если U4 > 0, сигнал "выключается" (скачок от U4 до 0,
    %      т.е. амплитуда = -U4)
    jumps = cell(3,1);
    for k = 1:3
        jumps{k}.time = [];         % Моменты времени скачков
        jumps{k}.amplitude = [];    % Амплитуды скачков

        % Скачок в начале: сигнал прыгает от 0 до U1
        if U1 > 0
            jumps{k}.time = [jumps{k}.time 0]; %#ok<*AGROW>
            jumps{k}.amplitude = [jumps{k}.amplitude U1];
        end

        % Скачок в точке перелома T1: сигнал прыгает от U2 до U3
        if U2 ~= U3
            jumps{k}.time = [jumps{k}.time T(k)];
            jumps{k}.amplitude = [jumps{k}.amplitude U3-U2];
        end

        % Скачок в конце: сигнал прыгает от U4 до 0
        if U4 > 0
            jumps{k}.time = [jumps{k}.time T2];
            jumps{k}.amplitude = [jumps{k}.amplitude -U4];
        end
    end

    % =====================================================================
    % ОПРЕДЕЛЕНИЕ НАКЛОНОВ (ЛИНЕЙНЫХ УЧАСТКОВ) СИГНАЛА
    % =====================================================================
    % Наклон — это участок, где сигнал линейно растёт или убывает.
    % Первая производная на таком участке = константа (значение наклона).
    % Вторая производная = 0 на участке, но на ГРАНИЦАХ наклона возникают
    % дельта-функции (от "включения" и "выключения" константы).
    %
    % Наклон записывается как:
    %   diff = (U_конец - U_начало) / (t_конец - t_начало)
    % Это просто значение производной (тангенс угла наклона).
    slopes = cell(3,1);
    for k = 1:3
        slopes{k}.time1 = [];   % Моменты начала наклонов
        slopes{k}.time2 = [];   % Моменты конца наклонов
        slopes{k}.diff = [];    % Значения наклонов (производных)

        % Первый отрезок [0, T1]: наклон = (U2 - U1) / T1
        % (если U2 == U1, наклон = 0, т.е. горизонтальная прямая — не записываем)
        if U2~=U1 && T(k)~=0
            slopes{k}.time1 = [slopes{k}.time1 0];
            slopes{k}.time2 = [slopes{k}.time2 T(k)];
            slopes{k}.diff = [slopes{k}.diff (U2-U1)/(T(k)-0)];
        end

        % Второй отрезок [T1, T2]: наклон = (U4 - U3) / (T2 - T1)
        if U4~=U3 && T2~=T(k)
            slopes{k}.time1 = [slopes{k}.time1; T(k)];
            slopes{k}.time2 = [slopes{k}.time2 T2];
            slopes{k}.diff = [slopes{k}.diff (U4-U3)/(T2-T(k))];
        end
    end

end
