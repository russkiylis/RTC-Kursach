classdef KursachSolver < handle
    % KURSACHSOLVER — класс, который автоматизирует расчёты курсовой работы
    % по дисциплине "Радиотехнические цепи и сигналы" (РТЦиС).
    %
    % СУТЬ КУРСОВОЙ (если совсем кратко):
    %   Есть входной видеоимпульс (кусочно-линейный сигнал) и шум.
    %   Нужно:
    %     1) Рассчитать спектр сигнала (через БПФ и аналитически).
    %     2) Синтезировать согласованный фильтр (СФ), который максимизирует
    %        отношение сигнал/шум (ОСШ) на выходе.
    %     3) Рассчитать прохождение сигнала и шума через СФ и RC-фильтр.
    %
    % КАК ПОЛЬЗОВАТЬСЯ:
    %   solver = KursachSolver(U1, U2, U3, U4, T, n, m, f_gr, T2, time_mult);
    %   solver.dano_showSignal();           % Посмотреть сигнал
    %   solver.dano_showNoiseSPM();         % Посмотреть спектр мощности шума
    %   solver.p1_showSpectrFFT();          % Посмотреть спектры через БПФ
    %   solver.p1_showSignalDiff();         % Посмотреть производные сигнала
    %   solver.p1_showSpectrAnalytical();   % Посмотреть аналитический спектр
    %
    % НАСЛЕДОВАНИЕ: класс наследуется от handle, что означает, что объект
    % передаётся по ссылке (как в Java/Python), а не копируется при передаче.

    properties

        % =====================================================================
        % ПАРАМЕТРЫ ИЗ ЗАДАНИЯ (индивидуальный вариант)
        % =====================================================================
        % Сигнал задан на отрезке [0, T2] двумя прямыми линиями:
        %   - Первый отрезок: от точки (0, U1) до точки (T1, U2)
        %   - Второй отрезок: от точки (T1, U3) до точки (T2, U4)
        % Если U2 ≠ U3, в точке T1 есть скачок (разрыв) сигнала.
        %
        % В задании даётся 3 значения T1 (вектор T), из которых потом
        % выбирается одно по критерию (min/max/mid граничной частоты спектра).

        U1;         % Напряжение сигнала в начальной точке t=0 (В)
        U2;         % Напряжение сигнала в конце первого отрезка, t=T1 (В)
        U3;         % Напряжение сигнала в начале второго отрезка, t=T1 (В)
                    %   (если U2 ≠ U3, то в точке T1 будет скачок)
        U4;         % Напряжение сигнала в конечной точке t=T2 (В)
        T;          % Вектор из 3 значений T1 — точка перелома сигнала (с)
                    %   Пример: [1 3 4].*1e-6 — три варианта в микросекундах
        n;          % Параметр для расчёта уровня шума (безразмерный)
                    %   W0 = umax^2 * T2 / n — амплитуда СПМ шума
        m;          % Параметр для расчёта граничной частоты шума (безразмерный)
                    %   omega_гр = 10*pi*m / T2
        f_gr;       % Критерий выбора сигнала: "min", "max" или "mid"
                    %   Определяет, какой из 3 вариантов T1 выбрать
                    %   (по граничной частоте спектра на уровне 0.1 от максимума)
        T2;         % Длительность всего сигнала (с). Сигнал определён на [0, T2]

        % =====================================================================
        % ОСИ ДЛЯ РАСЧЁТОВ И ПОСТРОЕНИЯ ГРАФИКОВ
        % =====================================================================
        N = 100000;     % Количество точек дискретизации (чем больше — тем точнее,
                        %   но медленнее). 100 000 — хороший компромисс.
        time;           % Ось времени — вектор значений t (с)
        cyclic_freq;    % Ось круговой частоты omega (рад/с)
                        %   Используется для аналитического спектра и СПМ шума
        freqFFT;        % Ось обычной частоты f (Гц) для БПФ-спектра
                        %   Связь: omega = 2*pi*f
        dt;             % Шаг дискретизации по времени (с)
                        %   dt = time(2) - time(1)

        % =====================================================================
        % ПАРАМЕТРЫ СИГНАЛА
        % =====================================================================
        u;              % Матрица сигналов [3 x N]: каждая строка — сигнал с
                        %   соответствующим значением T1 из вектора T.
                        %   u(k, :) — k-й вариант сигнала.
        umax;           % Максимальное значение сигнала (В) — нужно для расчёта
                        %   параметров шума (W0 зависит от umax).
        jumps;          % Структура с информацией о скачках (разрывах) сигнала.
                        %   jumps.time — моменты времени, где происходят скачки.
                        %   jumps.amplitude — амплитуды скачков.
                        %   Скачки порождают дельта-функции в первой производной.
        slopes;         % Структура с информацией о наклонах (линейных участках).
                        %   slopes.time1, slopes.time2 — начало и конец отрезка.
                        %   slopes.diff — значение наклона (производной) на отрезке.
                        %   Наклоны порождают ступеньки в первой производной
                        %   и дельта-функции во второй производной.
        time_mult;      % Множитель времени для красивого отображения.
                        %   Например, 1e-6 означает, что время в микросекундах.
                        %   Используется только для форматирования подписей.

        % =====================================================================
        % ПАРАМЕТРЫ ШУМА (СПМ — Спектральная Плотность Мощности)
        % =====================================================================
        % Шум моделируется как квазибелый: его СПМ постоянна (= W0) в полосе
        % частот |omega| < omega_гр, и равна нулю за пределами этой полосы.
        % Это упрощённая модель реального шума.
        noise_SPM;      % Вектор значений СПМ шума W(omega) (В^2/Гц)
        W0;             % Амплитуда (высота) СПМ шума (В^2/Гц)
                        %   W0 = umax^2 * T2 / n
        omega_gr_n;     % Граничная круговая частота шума (рад/с)
                        %   omega_гр = 10*pi*m / T2

        % =====================================================================
        % ПАРАМЕТРЫ СПЕКТРА СИГНАЛА
        % =====================================================================
        spectrFFT;      % Матрица [3 x N_pad] — спектры сигналов, посчитанные
                        %   через БПФ (быстрое преобразование Фурье).
                        %   spectrFFT(k,:) — комплексный спектр k-го варианта.
        zpad;           % Коэффициент zero-padding (дополнение нулями).
                        %   Увеличивает количество точек в частотной области,
                        %   что делает спектр визуально более гладким.
        f_gr01_FFT;     % Вектор [3x1] — граничные частоты (Гц) для каждого
                        %   варианта сигнала. Определяются по уровню 0.1 от
                        %   максимума амплитудного спектра (т.е. частота, правее
                        %   которой спектр не превышает 10% от пика).
        selectedSignal; % Индекс (1, 2 или 3) выбранного варианта сигнала
                        %   (по критерию f_gr — min/max/mid граничной частоты).
        spectrAnalytical_zveno; % Аналитический спектр, разложенный по звеньям
                        %   (каждое звено — вклад одного слагаемого в формуле).
                        %   Матрица, каждая строка — отдельное звено.
        spectrAnalytical;   % Суммарный аналитический спектр (сумма всех звеньев).
                        %   Комплексный вектор S(omega).

        % =====================================================================
        % ПАРАМЕТРЫ СОГЛАСОВАННОГО ФИЛЬТРА
        % =====================================================================    
        A;              % Постоянный множитель А
        K_SF;           % ПФ СФ
        K_SF_zveno      % ПФ СФ по звеням
        K_SF_text_zveno
        K_SF_simplified_terms   % Упрощённые звенья СФ (struct array: .coeff, .delay, .order, .type)
        K_SF_simplified_text    % LaTeX-формула упрощённой ПФ СФ

    end

    methods
        function obj = KursachSolver(U1, U2, U3, U4, T, n, m, f_gr, T2, time_mult)
            % КОНСТРУКТОР — создаёт объект и сразу выполняет все расчёты.
            %
            % Входные параметры:
            %   U1, U2, U3, U4 — амплитуды сигнала в ключевых точках (В)
            %   T              — вектор из 3 значений T1 (с), например [1 3 4]*1e-6
            %   n, m           — параметры шума (см. описание свойств выше)
            %   f_gr           — критерий выбора: "min", "max" или "mid"
            %   T2             — общая длительность сигнала (с)
            %   time_mult      — множитель времени (например, 1e-6 для мкс)
            %
            % При создании объекта автоматически выполняются ВСЕ расчёты:
            %   1. Генерация сигнала по заданным параметрам
            %   2. Расчёт СПМ шума
            %   3. Расчёт спектров через БПФ + выбор варианта сигнала
            %   4. Расчёт аналитического спектра выбранного сигнала

            obj.U1 = U1;
            obj.U2 = U2;
            obj.U3 = U3;
            obj.U4 = U4;
            obj.T = T;
            obj.n = n;
            obj.m = m;
            obj.f_gr = f_gr;
            obj.T2 = T2;
            obj.time_mult = time_mult;

            % Шаг 1: Генерируем кусочно-линейный сигнал по параметрам задания.
            %   Создаёт 3 варианта сигнала (для 3 значений T1), а также
            %   определяет, где в сигнале есть скачки и наклоны — это нужно
            %   для аналитического расчёта спектра методом дифференцирования.
            [obj.time, obj.dt, obj.u, obj.jumps, obj.slopes, obj.umax] = dano_createSignal(obj);

            % Шаг 2: Генерируем СПМ (спектральную плотность мощности) шума.
            %   Квазибелый шум: W(omega) = W0 при |omega| < omega_гр, иначе 0.
            [obj.cyclic_freq, obj.noise_SPM, obj.W0, obj.omega_gr_n] = dano_createNoiseSPM(obj);

            % Шаг 3: Считаем спектры всех 3 вариантов через БПФ (FFT) и
            %   выбираем один вариант по критерию f_gr (min/max/mid граничной
            %   частоты). После этого obj.selectedSignal содержит индекс
            %   выбранного варианта, а obj.slopes и obj.jumps обновлены
            %   для выбранного сигнала.
            obj = p1_createSpectrFFT(obj);

            % Шаг 4: Для выбранного сигнала строим аналитический спектр
            %   методом двукратного дифференцирования кусочно-линейной функции.
            %   Это даёт формулу спектра в замкнутом виде (без численного FFT).
            obj = p1_createSpectrAnalytical(obj);

        end
    end

    methods (Access=protected)
        % Защищённые методы — вызываются только изнутри класса (из конструктора).
        % Пользователь не может вызвать их напрямую.

        %--ДАНО--%
        [time, dt, u, jumps, slopes, umax] = dano_createSignal(obj)    % Создание сигнала
        [cyclic_freq, noise_SPM, W0, omega_gr_n] = dano_createNoiseSPM(obj)     % Создание СПМ шума

        %--ПУНКТ 1 (расчёт спектров)--%
        obj = p1_createSpectrFFT(obj);              % Численный спектр (БПФ)
        obj = p1_createSpectrAnalytical(obj);       % Аналитический спектр
    end

    methods
        % Публичные методы визуализации — их вызывает пользователь, чтобы
        % построить графики. Все расчёты уже выполнены в конструкторе,
        % эти методы просто рисуют результаты.

        %--ДАНО--%
        dano_showSignal(obj)     % Построить графики 3 вариантов входного сигнала
        dano_showNoiseSPM(obj)   % Построить график СПМ шума

        %--ПУНКТ 1--%
        p1_showSpectrFFT(obj)  % Построить амплитудные спектры (БПФ) для 3 вариантов
        p1_showSignalDiff(obj) % Построить первую и вторую производные сигнала
        p1_showSpectrAnalytical(obj)    % Построить аналитический спектр

        %--ПУНКТ 2--%
        p2_createSF(obj)    % Найти функцию согласованного фильтра
        p2_simplifySF(obj)  % Упростить ПФ СФ + нарисовать структурную схему
        p2_showSF(obj)      % Импульсная характеристика на узлах + АЧХ/ФЧХ
    end
end
