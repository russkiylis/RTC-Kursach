function [cyclic_freq, noise_SPM, W0, omega_gr_n] = dano_createNoiseSPM(obj)
% DANO_CREATENOISESPM — создаёт спектральную плотность мощности (СПМ) шума.
%
% ЧТО ТАКОЕ СПМ:
%   СПМ (спектральная плотность мощности) W(ω) показывает, как мощность
%   шума распределена по частотам. Размерность: В²/Гц (или В²·с).
%   Площадь под графиком W(ω) — это дисперсия (средняя мощность) шума.
%
% МОДЕЛЬ ШУМА В КУРСОВОЙ:
%   Используется квазибелый шум — упрощённая модель реального шума:
%     W(ω) = W0,   при |ω| < ω_гр   (СПМ постоянна в полосе частот)
%     W(ω) = 0,    при |ω| ≥ ω_гр   (вне полосы шума нет)
%
%   График W(ω) — прямоугольник высотой W0, шириной 2*ω_гр,
%   симметричный относительно ω = 0.
%
%   "Белый" шум имеет бесконечную полосу (W = const для всех ω),
%   квазибелый — ограниченную. Это ближе к реальности: в любой физической
%   системе полоса частот конечна.
%
% ФОРМУЛЫ ИЗ ЗАДАНИЯ:
%   W0 = umax² * T2 / n         — амплитуда СПМ
%   ω_гр = 10 * π * m / T2      — граничная круговая частота шума
%
%   где umax — максимальное значение входного сигнала,
%       n, m — параметры из индивидуального задания.
%
% ВЫХОДНЫЕ ПАРАМЕТРЫ:
%   cyclic_freq — ось круговой частоты ω (рад/с)
%   noise_SPM   — вектор значений СПМ W(ω) (В²/Гц)
%   W0          — амплитуда СПМ (В²/Гц)
%   omega_gr_n  — граничная круговая частота шума (рад/с)

    n = obj.n;
    m = obj.m;
    umax = obj.umax;   % Максимум входного сигнала (определён при создании сигнала)
    T2 = obj.T2;
    N = obj.N;

    % Расчёт параметров шума по формулам из задания
    W0 = (umax.^2.*T2)./n;         % Амплитуда СПМ: W0 = umax² * T2 / n
    omega_gr_n = (10.*pi.*m)./T2;   % Граничная частота: ω_гр = 10πm / T2

    % Ось круговой частоты: от -1.5*ω_гр до +1.5*ω_гр (с запасом для графика)
    cyclic_freq = linspace(-1.5*omega_gr_n, 1.5*omega_gr_n, N);

    % Маска частот, где СПМ ненулевая: |ω| < ω_гр
    kusok = (cyclic_freq > -omega_gr_n) & (cyclic_freq < omega_gr_n);

    % Заполняем СПМ: W0 внутри полосы, 0 снаружи
    noise_SPM = zeros(1,length(cyclic_freq));
    noise_SPM(kusok) = W0;

end
