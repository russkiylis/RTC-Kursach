function dano_showSignal(obj)
% DANO_SHOWSIGNAL — строит графики 3 вариантов входного сигнала u(t).
%
% Для каждого значения T1 из вектора T создаёт отдельное окно (figure)
% с графиком соответствующего кусочно-линейного сигнала.
%
% Нули [-T2, ...] и [..., 3*T2] добавлены по краям массива, чтобы
% на графике было видно, что сигнал равен нулю вне интервала [0, T2].

time = obj.time;
u = obj.u;
T = obj.T;
T2 = obj.T2;
umax = obj.umax;

for k = 1:3
    figure(name="Сигнал, T1 = "+num2str(T(k))+" с");

    % Рисуем сигнал. Добавляем точки (−T2, 0) слева и (3T2, 0) справа,
    % чтобы линия уходила в ноль за пределами [0, T2].
    plot([-T2 time 3.*T2], [0 u(k, :) 0], LineWidth=3);
    grid on;

    % Ограничиваем оси, чтобы было видно весь сигнал с небольшими отступами
    xlim([-0.1.*T2 1.2.*T2]);
    ylim([-0.5 1.2.*umax]);

    % Помещаем оси X и Y через начало координат (как в учебниках)
    ax = gca;
    ax.XAxisLocation = 'origin';    % Ось X проходит через y=0
    ax.YAxisLocation = 'origin';    % Ось Y проходит через x=0

    title("Сигнал, T1 = "+num2str(T(k))+" с");
    xlabel("t, с");
    ylabel("u(t), В");
end


end
