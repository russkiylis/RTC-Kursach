function obj = p1_createSpectrAnalytical(obj)
% P1_CREATESPECTRANALYTICAL — расчёт спектра сигнала аналитическим методом.
%
% МЕТОД ДИФФЕРЕНЦИРОВАНИЯ КУСОЧНО-ЛИНЕЙНОЙ ФУНКЦИИ:
%   Это ключевой метод в курсовой. Идея в том, что напрямую считать
%   преобразование Фурье от кусочно-линейной функции — сложно (нужно
%   интегрировать по частям). Но есть хитрость:
%
%   1) Берём сигнал u(t) — кусочно-линейная функция.
%
%   2) Считаем ПЕРВУЮ производную u'(t):
%      - На линейных участках производная = константа (наклон прямой).
%      - В точках скачков (разрывов) появляются дельта-функции δ(t-t0)
%        с амплитудой, равной величине скачка.
%      Результат: u'(t) = сумма прямоугольных ступенек + сумма δ-функций.
%
%   3) Считаем ВТОРУЮ производную u''(t):
%      - Ступеньки при дифференцировании дают дельта-функции на своих
%        границах (начало ступеньки → δ, конец → −δ).
%      - Дельта-функции из u'(t) при дифференцировании дают производные
%        дельта-функций δ'(t-t0).
%      Результат: u''(t) = сумма δ-функций + сумма δ'-функций.
%
%   4) Спектр δ-функций и δ'-функций известен аналитически:
%        F{A·δ(t-t0)} = A·e^{-jωt0}          — экспонента
%        F{A·δ'(t-t0)} = A·jω·e^{-jωt0}      — экспонента × jω
%
%   5) Если S''(ω) — спектр u''(t), то спектр исходного сигнала:
%        S(ω) = S''(ω) / (jω)²
%      потому что дифференцирование во временной области ↔ умножение
%      на jω в частотной области. Два дифференцирования → (jω)².
%
% ИТОГО: спектр кусочно-линейного сигнала записывается как сумма слагаемых:
%   - От дельта-функций второй производной (наклоны):
%       A_k · e^{-jωt_k} / (jω)²
%   - От δ-функций первой производной (скачки):
%       B_k · e^{-jωt_k} / (jω)
%
%   Каждое такое слагаемое — это "звено" (spectrAnalytical_zveno).
%   Суммарный спектр — их сумма (spectrAnalytical).
%
% БОНУС: формула спектра выводится в LaTeX-формате в консоль,
%   чтобы можно было вставить в пояснительную записку (Word/LaTeX).

jumps = obj.jumps;      % Скачки выбранного сигнала
slopes = obj.slopes;    % Наклоны выбранного сигнала
omega = obj.cyclic_freq; % Ось круговой частоты ω (рад/с)

sp = [];        % Матрица звеньев спектра (каждая строка — одно звено)
sp_text = [];   % Строка с LaTeX-формулой спектра

% =====================================================================
% ЧАСТЬ 1: ВКЛАДЫ ОТ НАКЛОНОВ → дельта-функции во ВТОРОЙ производной
% =====================================================================
% Каждый линейный участок с наклоном d на отрезке [t1, t2] даёт:
%   - Вклад во вторую производную: d·δ(t-t1) - d·δ(t-t2)
%     (ступенька "включается" в t1 и "выключается" в t2)
%   - Вклад в спектр: d·e^{-jωt1}/(jω)² - d·e^{-jωt2}/(jω)²
%
% Сначала собираем все дельта-функции от всех наклонов.
% Если несколько дельт попадают в одну точку (например, конец одного
% наклона совпадает с началом другого), их амплитуды складываются.
imp_times = [slopes.time1(:)', slopes.time2(:)'];       % Моменты всех импульсов
imp_amps  = [slopes.diff(:)', -slopes.diff(:)'];        % Амплитуды (минус для конца)

% Объединяем дельта-функции в одинаковых точках времени:
% unique() находит уникальные моменты, accumarray() суммирует амплитуды
[unique_times, ~, idx] = unique(imp_times);
combined_amps = accumarray(idx, imp_amps);

% Формируем звенья спектра от дельта-функций второй производной
% Формула звена: A · e^{-jωt} / (jω)²
for k = 1:length(combined_amps)
    if isempty(sp)
        sp = combined_amps(k).*exp(-1i.*omega.*unique_times(k))./(1i.*omega).^2;
        sp_text = sprintf("%.10g",combined_amps(k))+"\frac{e^{-j\omega"+sprintf("%.10g",unique_times(k))+"}}{(j\omega)^2}";
        obj.K_SF_text_zveno = sprintf("%.10g",combined_amps(k))+"\frac{e^{j\omega"+sprintf("%.10g",unique_times(k))+"}}{(-j\omega)^2}";
    else
        sp = [sp; combined_amps(k).*exp(-1i.*omega.*unique_times(k))./(1i.*omega).^2];     %#ok<*AGROW>
        sp_text = sp_text+" + "+sprintf("%.10g",combined_amps(k))+"\frac{e^{-j\omega"+sprintf("%.10g",unique_times(k))+"}}{(j\omega)^2}";
        obj.K_SF_text_zveno = [obj.K_SF_text_zveno sprintf("%.10g",combined_amps(k))+"\frac{e^{j\omega"+sprintf("%.10g",unique_times(k))+"}}{(-j\omega)^2}"];
    end
end

% =====================================================================
% ЧАСТЬ 2: ВКЛАДЫ ОТ СКАЧКОВ → дельта-функции в ПЕРВОЙ производной
% =====================================================================
% Скачок амплитудой B в точке t0 даёт:
%   - Вклад в первую производную: B·δ(t-t0)
%   - Вклад в спектр: B·e^{-jωt0}/(jω)
%     (делим на (jω) вместо (jω)², потому что это вклад первой,
%      а не второй производной — "интегрируем" один раз вместо двух)
for k = 1:length(jumps.amplitude)
    if isempty(sp)
        sp = jumps.amplitude(k).*exp(-1i.*omega.*jumps.time(k))./(1i.*omega);
        sp_text = sprintf("%.10g",jumps.amplitude(k))+"\frac{e^{-j\omega"+sprintf("%.10g",jumps.time(k))+"}}{j\omega}";
        obj.K_SF_text_zveno = sprintf("%.10g",jumps.amplitude(k))+"\frac{e^{j\omega"+sprintf("%.10g",jumps.time(k))+"}}{-j\omega}";
    else
        sp = [sp; jumps.amplitude(k).*exp(-1i.*omega.*jumps.time(k))./(1i.*omega)];
        sp_text = sp_text+" + "+sprintf("%.10g",jumps.amplitude(k))+"\frac{e^{-j\omega"+sprintf("%.10g",jumps.time(k))+"}}{j\omega}";
        obj.K_SF_text_zveno = [obj.K_SF_text_zveno sprintf("%.10g",jumps.amplitude(k))+"\frac{e^{j\omega"+sprintf("%.10g",jumps.time(k))+"}}{-j\omega}"];
    end
end

% Выводим итоговую формулу спектра в LaTeX-формате
% (можно скопировать в Word через вставку формулы или в LaTeX-документ)
disp("Аналитический спектр (латех, можно вставить в ворд):");
disp(sp_text);

% Сохраняем: отдельные звенья и суммарный спектр
obj.spectrAnalytical_zveno = sp;        % Матрица звеньев [M x N_freq]
obj.spectrAnalytical = sum(sp,1);       % Суммарный спектр [1 x N_freq]
