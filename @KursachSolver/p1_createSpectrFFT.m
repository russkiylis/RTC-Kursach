function obj = p1_createSpectrFFT(obj)
% P1_CREATESPECTRFFT — расчёт спектров сигнала численным методом (через БПФ/FFT).
%
% ЧТО ТАКОЕ БПФ (FFT):
%   БПФ (быстрое преобразование Фурье, англ. FFT) — алгоритм, который
%   вычисляет дискретное преобразование Фурье (ДПФ) сигнала. По сути,
%   он раскладывает сигнал на сумму синусоид разных частот и амплитуд.
%
%   Результат FFT — комплексный вектор S(f), где:
%     |S(f)| — амплитудный спектр (какая амплитуда у каждой частоты)
%     arg(S(f)) — фазовый спектр (какая фаза у каждой частоты)
%
% ЗАЧЕМ ZERO-PADDING:
%   FFT даёт столько частотных точек, сколько было временных.
%   Zero-padding (дополнение нулями) — это приписывание нулей к сигналу
%   перед FFT. Это НЕ добавляет новой информации, но увеличивает количество
%   точек в частотной области → график спектра становится более гладким.
%   Здесь zpad=100, т.е. длина увеличивается в 100 раз.
%
% ЗАЧЕМ fftshift:
%   Стандартная функция fft() в Matlab возвращает спектр в "сырой" форме:
%   сначала положительные частоты, потом отрицательные. Функция fftshift()
%   переставляет половинки местами, чтобы спектр шёл от -fmax до +fmax
%   (как в учебниках).
%
% УМНОЖЕНИЕ НА dt:
%   fft() вычисляет ДПФ (дискретную сумму), а нам нужно приближение
%   непрерывного преобразования Фурье. Умножение на dt (шаг дискретизации)
%   даёт корректную размерность В/Гц (= В·с).
%
% ВЫБОР СИГНАЛА ПО КРИТЕРИЮ:
%   После расчёта спектров для всех 3 вариантов T1 определяется граничная
%   частота f_гр для каждого (частота, правее которой |S(f)| < 0.1 * max|S(f)|).
%   Затем выбирается один вариант по критерию f_gr ("min"/"max"/"mid").

    % Частота дискретизации: fs = 1/dt (по теореме Найквиста-Шеннона,
    % максимальная частота в спектре = fs/2)
    fs = 1/obj.dt;

    % Zero-padding: увеличиваем длину сигнала в zpad раз, дополняя нулями
    obj.zpad = 100;
    N_pad = obj.N * obj.zpad;   % Итоговая длина = 100 000 * 100 = 10 000 000

    % =====================================================================
    % СОЗДАНИЕ ОСИ ЧАСТОТЫ
    % =====================================================================
    % Ось обычной частоты f (Гц), центрированная вокруг 0.
    % Формула: f_k = (k - N_pad/2) * (fs / N_pad), k = 0, 1, ..., N_pad-1
    % Это даёт равномерную сетку от -fs/2 до +fs/2.
    obj.freqFFT = (-N_pad/2 : N_pad/2-1) * (fs / N_pad);

    % =====================================================================
    % РАСЧЁТ FFT-СПЕКТРОВ ДЛЯ ВСЕХ 3 ВАРИАНТОВ
    % =====================================================================
    obj.spectrFFT = zeros(3,length(obj.u(1,:))*obj.zpad);
    obj.f_gr01_FFT = zeros(3,1);

    for k = 1:3
        % fft(signal, N_pad) — вычисляет FFT с zero-padding до длины N_pad.
        % fftshift() — сдвигает спектр, чтобы нулевая частота была по центру.
        % * obj.dt — переход от ДПФ к приближению непрерывного преобразования Фурье.
        obj.spectrFFT(k,:) = fftshift(fft(obj.u(k,:),N_pad)) .* obj.dt;

        % Определяем граничную частоту по уровню 0.1 от максимума:
        % Находим ПОСЛЕДНЮЮ точку, где |S(f)| ≥ 0.1 * max|S(f)|.
        % Это и есть f_гр — частота, правее которой спектр "затухает".
        obj.f_gr01_FFT(k,1) = obj.freqFFT(find(abs(obj.spectrFFT(k,:))>=max(abs(obj.spectrFFT(k,:))*0.1),1,"last"));
    end

    % =====================================================================
    % ВЫБОР ВАРИАНТА СИГНАЛА ПО КРИТЕРИЮ
    % =====================================================================
    % В задании сказано: из 3 вариантов выбрать тот, у которого граничная
    % частота минимальна ("min"), максимальна ("max") или средняя ("mid").
    % Дальше в курсовой работаем только с выбранным сигналом.
    f = obj.f_gr01_FFT(:,1);          % Вектор граничных частот [3x1]

    [~, idx_max] = max(f);     % Индекс сигнала с максимальной f_гр
    [~, idx_min] = min(f);     % Индекс сигнала с минимальной f_гр

    if obj.f_gr == "max"
        obj.selectedSignal = idx_max;       % Выбираем с наибольшей f_гр

    elseif obj.f_gr == "min"
        obj.selectedSignal = idx_min;       % Выбираем с наименьшей f_гр

    else
        % "mid" — выбираем тот, который не max и не min (средний)
        all_idx = 1:length(f);
        mid_idx = all_idx(all_idx ~= idx_max & all_idx ~= idx_min);
        obj.selectedSignal = mid_idx(1);
    end

    % После выбора: обновляем slopes и jumps, чтобы они соответствовали
    % только выбранному сигналу (а не cell-массиву для всех 3 вариантов).
    % Это нужно для дальнейшего аналитического расчёта спектра.
    obj.slopes = obj.slopes{obj.selectedSignal};
    obj.jumps = obj.jumps{obj.selectedSignal};

end
