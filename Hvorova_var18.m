% =========================================================================
% ВАРИАНТ 18 — Хворова
% =========================================================================
% Точка входа для расчёта курсовой работы.
% Здесь задаются только индивидуальные параметры варианта, а все расчёты
% выполняет общий класс KursachSolver.
% =========================================================================

clear;      % Очистить рабочую область MATLAB
clc;        % Очистить командное окно
close all;  % Закрыть старые графики

% =========================================================================
% ПАРАМЕТРЫ ИЗ ИНДИВИДУАЛЬНОГО ЗАДАНИЯ
% =========================================================================
U1 = 9;
U2 = 7;
U3 = 2;
U4 = 0;

% Три возможных значения T1. Конкретный сигнал выбирается по критерию f_gr.
T = [1 3 4].*1e-6;

n = 18;         % Параметр шума: W0 = umax^2*T2/n
m = 10;         % Параметр полосы шума: omega_gr = 10*pi*m/T2
f_gr = "mid";   % Критерий выбора сигнала: "min", "mid" или "max"
T2 = 5e-6;      % Общая длительность сигнала

% Последний аргумент 1e-6 означает, что подписи времени будут в мкс.
solver = KursachSolver(U1, U2, U3, U4, T, n, m, f_gr, T2, 1e-6);

% =========================================================================
% ДАНО
% =========================================================================
solver.dano_showSignal();
solver.dano_showNoiseSPM();

% =========================================================================
% ПУНКТ 1 — спектры выбранного сигнала
% =========================================================================
solver.p1_showSpectrFFT();
solver.p1_showSignalDiff();
solver.p1_showSpectrAnalytical();

% =========================================================================
% ПУНКТ 2 — согласованный фильтр
% =========================================================================
solver.p2_createSF();
solver.p2_simplifySF();
solver.p2_showSF();

% =========================================================================
% ПУНКТ 3 — сигнал и шум на выходе согласованного фильтра
% =========================================================================
solver.p33_showCorrFunc();
solver.p32_showOutputNoiseSPM();
solver.p33_showNoiseStats();
solver.p34_showSNR();
solver.p35_showRectResponse();
solver.p36_showRectSNR();
solver.p33_showCorrFormulas();
solver.p33_verifyCorrFunc();

% =========================================================================
% ПУНКТ 4 — квазиоптимальный RC-фильтр
% =========================================================================
% Точки tau задаём явно как множители относительно tau0 = 1/f_0.1.
% Если нужно изменить таблицу для RC-фильтра, меняйте только rcTauFactors.
rcTauFactors = 0.1:0.1:2.4;
rcTau0 = 1 / abs(solver.f_gr01_FFT(solver.selectedSignal));
rcTauValues = rcTauFactors .* rcTau0;

solver.p41_calcRCResponses(rcTauValues);
solver.p44_calcRCNoiseStats("stats");
solver.p45_calcRCSNR();
solver.p46_showRCOptimalComparison();
solver.p49_showRectRCResponse();
solver.p44_calcRCNoiseStats("spm");
