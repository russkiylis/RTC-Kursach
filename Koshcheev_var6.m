% Этот файл — точка входа. Здесь задаются параметры из индивидуального
% задания и запускаются все расчёты и визуализации.
%
% КАК ЗАПУСТИТЬ: открой этот файл в Matlab и нажми F5 (Run).
% Откроются окна с графиками сигнала, шума, спектров и производных.
% =========================================================================

clear;      % Очищаем все переменные из рабочей области (workspace)
clc;        % Очищаем командное окно (Command Window)
close all;  % Закрываем все открытые окна с графиками

% =========================================================================
% ПАРАМЕТРЫ ИЗ ИНДИВИДУАЛЬНОГО ЗАДАНИЯ
% =========================================================================
% Сигнал u(t) задан на [0, T2] двумя прямыми:
%   Первый отрезок: от (0, U1=6) до (T1, U2=6) — горизонтальная прямая
%   В точке T1: скачок с U2=6 до U3=4 (т.к. U2 ≠ U3)
%   Второй отрезок: от (T1, U3=4) до (T2, U4=0) — линейный спад
%
%   Визуально:
%     6 ─────────┐
%                 │4
%                 └──────── 0
%     0         T1        T2

U1 = 6;     % Напряжение в начальной точке t=0 (В)
U2 = 6;     % Напряжение в конце первого отрезка (В)
U3 = 4;     % Напряжение в начале второго отрезка (В) — скачок с 6 до 4
U4 = 0;     % Напряжение в конечной точке t=T2 (В)

% Три варианта T1 (точка перелома) в микросекундах.
% Из них будет выбран один по критерию f_gr.
T = [1 3 4].*1e-6;     % T1 = 1 мкс, 3 мкс или 4 мкс

n = 14;         % Параметр шума: W0 = umax² * T2 / n
m = 40;         % Параметр шума: ω_гр = 10πm / T2

% Критерий выбора варианта сигнала:
%   "max" — выбрать вариант с наибольшей граничной частотой спектра
%   "min" — с наименьшей
%   "mid" — со средней
f_gr = "max";

T2 = 5e-6;     % Общая длительность сигнала: 5 мкс

% =========================================================================
% СОЗДАНИЕ ОБЪЕКТА-РЕШАТЕЛЯ
% =========================================================================
% При создании объекта KursachSolver автоматически выполняются ВСЕ расчёты:
%   1. Генерация 3 вариантов кусочно-линейного сигнала
%   2. Расчёт СПМ (спектральной плотности мощности) шума
%   3. Расчёт спектров всех 3 вариантов через БПФ (FFT)
%   4. Выбор одного варианта по критерию f_gr
%   5. Аналитический расчёт спектра выбранного сигнала
%
% Последний аргумент (1e-6) — множитель времени, означает, что время
% задано в микросекундах. Используется для корректных подписей на графиках.
solver = KursachSolver(U1, U2, U3, U4, T, n, m, f_gr, T2, 1e-6);

% =========================================================================
% ВИЗУАЛИЗАЦИЯ РЕЗУЛЬТАТОВ
% =========================================================================

% Графики 3 вариантов входного сигнала u(t) — по одному окну на вариант
solver.dano_showSignal();

% График СПМ шума W(ω) — прямоугольник высотой W0 в полосе ±ω_гр
solver.dano_showNoiseSPM();

% Амплитудные спектры |S(f)| для всех 3 вариантов (через БПФ).
% Выбранный вариант выделен красным. Отмечены граничные частоты f_гр.
solver.p1_showSpectrFFT();

% Первая и вторая производные выбранного сигнала.
% Нужны для понимания метода дифференцирования (аналитический спектр).
solver.p1_showSignalDiff();

% Аналитический амплитудный спектр выбранного сигнала.
% Должен совпадать с FFT-спектром — это проверка правильности формул.
solver.p1_showSpectrAnalytical();
